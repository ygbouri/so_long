#include"ft_printf.h"

int	ft_count_nbr_hexa(unsigned long n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n *= -1;
		i++;
	}
	while (n > 0)
	{
		n = n / 16;
		i++;
	}
	return (i);
}
